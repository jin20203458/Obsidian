// ============================================================================
// DAPA Software Coding Guidelines - Cppcheck Detected (True Positive) Test Suite
// Cppcheck가 검출하는 데 성공한 40종의 규칙 위반 사례(Bad Case) 모음
// ============================================================================

#include <iostream>
#include <vector>
#include <string>
#include <cstdlib>
#include <cstdio>
#include <cstring>
#include <stdexcept>
#include <algorithm>

// ----------------------------------------------------------------------------
// Rule 14: [Common_Init] Bullet 나 - 변수는 사용전 반드시 초기화되어야 한다.
// ----------------------------------------------------------------------------
namespace Rule_14 {
    void uninit_var() {
        int x;
        int y = x + 10; // ❌ 위반: 초기화되지 않은 변수 x 사용
    }
}

// ----------------------------------------------------------------------------
// Rule 15: [Common_Init] Bullet 다 - 값이 초기화되지 않은 변수의 포인터를 함수의 Read-only(const)로 사용해서는 안된다.
// ----------------------------------------------------------------------------
namespace Rule_15 {
    void read_only_const(const int* p) {
        int val = *p;
    }
    void bad_call() {
        int y;
        read_only_const(&y); // ❌ 위반: 초기화되지 않은 y의 주소 전달
    }
}

// ----------------------------------------------------------------------------
// Rule 19: [Common_Ident] Bullet 라 - 외부 scope에서 정의된 변수를 내부 scope에서 재정의 해서는 안된다.
// ----------------------------------------------------------------------------
namespace Rule_19 {
    int variable = 100;
    void shadow_test() {
        int variable = 200; // ❌ 위반: 전역 변수 'variable'을 로컬에서 재정의 (Shadowing)
    }
}

// ----------------------------------------------------------------------------
// Rule 21: [Common_Cond] Bullet 나 - 조건문의 결과가 항상 True 혹은 False라면 이는 조건문으로 작성해서는 안된다.
// ----------------------------------------------------------------------------
namespace Rule_21 {
    void constant_condition() {
        int y = 10;
        if (y < 30) { // ❌ 위반: 결과가 항상 참인 조건문
            std::cout << "Always True\n";
        }
    }
}

// ----------------------------------------------------------------------------
// Rule 24: [Common_Cond] Bullet 마 - 수행되지 않는 소스코드를 작성하지 말아야 한다.
// ----------------------------------------------------------------------------
namespace Rule_24 {
    void unreachable_test(int n) {
        return;
        n = 100; // ❌ 위반: return문 뒤의 도달할 수 없는 코드
    }
}

// ----------------------------------------------------------------------------
// Rule 25: [Common_Conv] Bullet 가 - 선언된 데이터 형으로 표현할 수 있는 숫자의 영역을 초과하는 값을 할당하지 말아야 한다.
// ----------------------------------------------------------------------------
namespace Rule_25 {
    void range_over() {
        char x = 300; // ❌ 위반: 1바이트 signed char 범위(-128~127)를 초과하는 값 할당
    }
}

// ----------------------------------------------------------------------------
// Rule 26: [Common_Conv] Bullet 나 - 가변인수를 받는 함수의 Conversion 지시자와 Argument의 type은 동일해야한다.
// ----------------------------------------------------------------------------
namespace Rule_26 {
    void bad_format() {
        printf("%s", 42); // ❌ 위반: %s 자리에 정수형 전달
    }
}

// ----------------------------------------------------------------------------
// Rule 27: [Common_Conv] Bullet 다 - 가변인수를 받는 함수의 Conversion 지시자와 Argument의 개수는 동일해야한다.
// ----------------------------------------------------------------------------
namespace Rule_27 {
    void bad_arg_count() {
        printf("%d %d", 10); // ❌ 위반: 지시자 2개, 매개변수 1개
    }
}

// ----------------------------------------------------------------------------
// Rule 33: [Common_PtrArray] Bullet 가 - 포인터는 역참조 하기 전에 NULL 인지를 반드시 확인해야 한다.
// ----------------------------------------------------------------------------
namespace Rule_33 {
    void null_deref_bad(int* ptr) {
        *ptr = 100; // ❌ 위반: NULL 체크 생략
    }
}

// ----------------------------------------------------------------------------
// Rule 34: [Common_PtrArray] Bullet 나 - 지역 변수의 주소 값을 처리하는 handle을 return하지 말아야 한다.
// ----------------------------------------------------------------------------
namespace Rule_34 {
    int* get_stack_addr() {
        int local_val = 42;
        return &local_val; // ❌ 위반: 로컬 스택 변수 주소 반환
    }
}

// ----------------------------------------------------------------------------
// Rule 35: [Common_PtrArray] Bullet 다 - 함수 parameter의 주소 값을 처리하는 handle을 return하지 말아야 한다.
// ----------------------------------------------------------------------------
namespace Rule_35 {
    int* get_param_addr(int param) {
        return &param; // ❌ 위반: 매개변수 주소 반환
    }
}

// ----------------------------------------------------------------------------
// Rule 36: [Common_PtrArray] Bullet 라 - 배열의 크기를 초과하는 인덱스로 참조하지 말아야 한다.
// ----------------------------------------------------------------------------
namespace Rule_36 {
    void array_overflow() {
        int arr[5] = {1, 2, 3, 4, 5};
        arr[5] = 100; // ❌ 위반: 인덱스 5 범위 초과 접근
    }
}

// ----------------------------------------------------------------------------
// Rule 37: [Common_PtrArray] Bullet 마 - Null Pointer를 역참조 하지 않는다.
// ----------------------------------------------------------------------------
namespace Rule_37 {
    void null_pointer_test() {
        int* p = nullptr;
        *p = 10; // ❌ 위반: 명시적 nullptr 역참조
    }
}

// ----------------------------------------------------------------------------
// Rule 41: [Common_Expr] Bullet 라 - 피연산자의 데이터 크기를 초과하는 비트만큼 Shift 연산을 수행하지 않는다.
// ----------------------------------------------------------------------------
namespace Rule_41 {
    void bad_shift() {
        int x = 1;
        int y = x << 32; // ❌ 위반: 32비트 변수를 32비트만큼 시프트
    }
}

// ----------------------------------------------------------------------------
// Rule 47: [C_Specific] Bullet 1 - Scanf의 Argument 에는 Object Value가 저장된 주소값이 입력되어야 한다.
// ----------------------------------------------------------------------------
namespace Rule_47 {
    void bad_scanf() {
        int n;
        scanf("%d", n); // ❌ 위반: 주소(&n)가 아닌 정수값 전달
    }
}

// ----------------------------------------------------------------------------
// Rule 52: [CPP_Specific_1] Bullet 1 - 동적할당된 변수를 release 할 때에는 변수의 type과 일치하게 release 하여야 한다.
// ----------------------------------------------------------------------------
namespace Rule_52 {
    void test_mismatched_delete() {
        int* p1 = new int[10];
        delete p1; // ❌ 위반: new[] 할당을 delete로 해제
    }
}

// ----------------------------------------------------------------------------
// Rule 53: [CPP_Specific_1] Bullet 2 - 지역 변수의 주소 값을 처리하는 handle을 return하지 말아야 한다. (C++)
// ----------------------------------------------------------------------------
namespace Rule_53 {
    int* return_stack() {
        int x = 100;
        return &x; // ❌ 위반: 지역 변수 주소 반환
    }
}

// ----------------------------------------------------------------------------
// Rule 54: [CPP_Specific_1] Bullet 3 - 함수 parameter의 주소 값을 처리하는 handle을 return하지 말아야 한다. (C++)
// ----------------------------------------------------------------------------
namespace Rule_54 {
    int* return_param(int x) {
        return &x; // ❌ 위반: 매개변수 주소 반환
    }
}

// ----------------------------------------------------------------------------
// Rule 59: [CPP_Specific_2] Bullet 8 - release 된 메모리 영역을 사용하지 말아야 한다. (UAF)
// ----------------------------------------------------------------------------
namespace Rule_59 {
    void use_after_free() {
        int* p = new int(10);
        delete p;
        std::cout << *p; // ❌ 위반: 이미 해제된 포인터 p 역참조
    }
}

// ----------------------------------------------------------------------------
// Rule 65: [CPP_Specific_2] Bullet 14 - 생성자/소멸자 내에서는 가상함수를 반드시 식별자(Qualifier)를 붙여서 호출해야 한다.
// ----------------------------------------------------------------------------
namespace Rule_65 {
    class BaseCtor {
    public:
        virtual void f() {}
        BaseCtor() {
            f(); // ❌ 위반: 생성자에서 가상 함수 직접 호출
        }
    };
}
